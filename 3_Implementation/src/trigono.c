#include <trigono.h>

#define PI 3.1423

float sine_fun(float var)
{
    return (sin(var));
}

float cosine_fun(float var)
{
    return (cos(var));
}
float tan_fun(float var)
{
    return (tan(var));
}
float cot_fun(float var)
{
    return (1/tan(var));
}
float sec_fun(float var)
{
    return (1/cos(var));
}
float cosec_fun(float var)
{
    return (1/sin(var));
}
